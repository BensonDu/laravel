        <h1>像特斯拉创始人马斯克一样思考</h1>
        <h6>硅谷创业奇人伊隆-马斯克(Elon Musk)被认为是乔布斯之后的下一个创新领袖，他在互联网支付（Paypal）、电动汽车（Tesla Motors）和太空探索（SpaceX）等三个迥然不同的领域创立了三家成功的公司。很多人都好奇：伊隆的思维方式和常人有那些不同？他是如何思考问题的？</h6>
        </div>
        <div class="social">
