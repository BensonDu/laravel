        <div class="content post_read">
           <p class=""><span style="line-height: 1.875;"> 人类社会已经迈入了「互联网 +」时代，互联网带给各行业的变革和发展有目共睹。艺术和互联网的跨界合作从最初的网上拍卖开始，经过十年的发展，无论从形式、效果还是口碑虽然还处于摸索阶段，但艺术品的线上交易是艺术品未来的发展趋势已经成为业内共识。此次自媒体账号 62 度灰于 12 月 18 日在尤伦斯中心召开的以「62 度灰和 TA 的朋友们」为主题的发布会，让互联网和艺术的跨界合作找到了一个新的契机——艺术家居。</span><br></p><div class="medium-insert-images"><figure contenteditable="false"><img src="//dn-noman.qbox.me/FiJU9fkI2igGIeTkZ_fAqwnqcqID" alt=""></figure><span style="line-height: 1.875;"></span></div><p> 这次发布会作为一次综合性的艺术展览，有超过 62 位中国当代艺术家携作品亮相。其中既包括在中国当代艺术界久负盛名的尚扬和徐累及李可染之子李玉双，也包括备受关注的中生代艺术家曾健勇、张天幕、朱敬一、陈鸿志、祝铮鸣、关晶晶等人，还有数十位风格各异的新生代艺术家参与此次盛会。他们中的大多数人都是 62 度灰以及背后《NO ART》杂志的老朋友，在这里他们的艺术作品、艺术观点将得到推介和分享。</p><div class="medium-insert-images"><figure contenteditable="false"><img src="//dn-noman.qbox.me/Fu5rs7BNZMSXfZgFIht_2utA88TP" alt=""></figure></div><p> 艺术不再是曲高和寡，而可以是直指生活、温暖心灵，可以让生活变得更加美好。艺术成为了一种媒介一种情感表达的方式，这正契合了 62 度灰艺术和互联网跨界合作艺术家居这个主题的。</p><p>62 度灰作为北京艺客文化传播有限公司旗下的新媒体平台，是和其旗下艺术品在线交易网站「艺客网」相辅相成的。艺客网承担在线艺术品交易的职能，而 62 度灰则承担艺术作品推介、艺术观点展示的职能。两者结合起来，让更多人通过 62 度灰了解艺术，然后用艺客网将艺术品带回家，这就建立了一个相对完整的产业链，也让艺术家没有了后顾之忧，可以专心创作。艺客网自 2014 年 10 月上线以来，截至到目前为止已经有超过 3000 个艺术家、超过 5 万件原创艺术品在线销售，月平均营业额超过千万元，已经成为国内最大的艺术品电商平台之一。</p><div class="medium-insert-images"><figure contenteditable="false"><img src="//dn-noman.qbox.me/Fl-CVOyxX8idVEcL7-v5CA1NgEwI" alt=""></figure></div><p> 北京艺客于 2015 年 1 月获多家投资机构 1000 万美元 A 轮投资，今年 10 月与豆瓣达成战略合作，进一步加速进军家装市场的步伐。接下来，「62 度灰」在为公众推介更多优秀艺术家的同时，也将致力于将公众导引向艺客网平台和手机端 app，在互联网线上、手机端和线下形成更紧密的连接。此次发布会邀请了众多媒体参与，一起将艺术带回家，让便捷的互联网将艺术更好的状态传递给更多的人。</p><p> 在此次发布会现场，62 度灰的执行出品人也会将 62 度灰未来的战略构想传递给大家，关于家居艺术、儿童艺术以及美学教育交流，从多个方面对 62 度灰的跨界合作进行详细阐述。但是这些跨界合作的基础是艺术要先运用互联网将观点更好地表达出去，这才是关键。</p><div class="medium-insert-images"><figure contenteditable="false"><img src="//dn-noman.qbox.me/FncLRspPjVKoERz9jOxcp9HBllWv" alt=""></figure></div><p> 现在，北京艺客文化传播有限公司已经与一百家互联网创业公司签署了艺术品租赁协议，其中包括滴滴打车、爱奇艺、唯品会、美团和豆瓣等大型公司。并与数百位艺术家签订了作品的版权授权协议，作为艺术版权管理和衍生品开发的先锋，62 度灰让互联网和艺术的跨界合作变得更加顺畅起来。</p><p> 这次发布会也是一场展览会、一场诗会、一场演唱会，有到场嘉宾的作品展览和著名音乐人莫西子诗为 62 度灰特别奉献的现场表演。12 月 19 日，主办方还邀请了诗人树才、绘本画家熊亮进行沙龙对谈，同时请来一群活泼可爱的孩子们进行诗歌朗诵，现场还搭配者来女等民谣歌手的演出。自媒体 62 度灰的发声日给互联网和艺术提供了更多的合作契机，北京艺客接下来会以艺术家居作为契合点，在互联网领域展开更加全面的合作，倾力打造出一条互联网艺术合作的新道路。</p><div class="medium-insert-images"><figure contenteditable="false"><img src="//dn-noman.qbox.me/Frlt0Q1goZ0zrcOLWQy2yu967-oG" alt=""></figure></div><p><br></p>
